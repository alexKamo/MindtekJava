package VariablesIntro;

public class VariableIntro {
    public static void main(String[] args) {
        // comment out
        // variables pattern: type varName = value;
        int num = 5;
        double decNum = 12.1;
        System.out.println(decNum);
        num = 10;
        System.out.println(num);
        num = 66;
        System.out.println(num);

        //type varName = value
        String word = "java";
        word = "mindtek";
        System.out.println(word);
        // word = 77;



    }
}

